import { Modal } from "antd";
import { useGlobalContext } from "./AppProvider";

const ProductSearch = () => {
  const { isModalOpen, setIsModalOpen } = useGlobalContext();

  const handleOk = () => {
    setIsModalOpen(false);
  };

  const handleCancel = () => {
    setIsModalOpen(false);
  };

  return (
    <>
      <Modal
        title="Basic Modal"
        open={isModalOpen}
        onOk={handleOk}
        onCancel={handleCancel}
      >
        <p>Some contents...</p>
        <p>Some contents...</p>
        <p>Some contents...</p>
      </Modal>
    </>
  );
};
export default ProductSearch;
